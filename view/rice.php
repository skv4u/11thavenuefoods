        <h3 class=" mb-4">RICE</h3>
       <p>Rice is a staple in any kitchen, and with so many different types, it lends itself to a limitless number of recipes. Before you add the same type of rice to every recipe you prepare, however, think twice. Substituting one type of rice for another can really alter the result of a recipe. Each type of rice has its own taste, texture, and unique properties that work well with different cooking applications. So how do you know which one is the best type of rice to use? This guide examines factors that differentiate types of rice, from nutty basmati to fragrant jasmine and more!</p>
<p>

<h3 class="mb-4">Glossary</h3>
<p>

The following anatomy terms may be used throughout this guide when discussing the differences in types of rice.
</p>

<ul>
    <li>
<p>
<strong>Hull</strong> – Each grain of rice is enclosed in a tough outer hull, or husk, that needs to be removed before it can be consumed. This layer is removed in all rice types.
</p></li>
<li><p>
<strong>Bran</strong> – Under the hull, the bran layer is not removed in all rice types. This nutritious whole grain section is usually tan-colored, but it may be reddish or black depending on the pigmentation in the bran layers. The bran layer may be consumed, but it is often removed when further processing rice.
</p></li>

<li><p>
<strong>White Rice </strong>– Once the bran and germ layers are removed, white rice remains. Known as the endosperm, this is the part of the rice that is most commonly consumed.
</p></li>

<li><p>
<strong>Germ </strong>– Found under the hull, the germ, or rice kernel, is nutrient-dense. Full of B vitamins, minerals, and proteins, it helps give rice its color and added nutritional benefits.</p></li>
</ul>	

<h3 class="mt-6 mb-4">Type Of Rice</h3>

<ul>
    <h4 class="mb-4">
Length and Shape</h4>


<li>
<p>
<strong>Rice is often characterized as one of three varieties</strong> – long grain, medium grain, or short grain rice. These varieties refer to the length and shape of the grain. Simply speaking, long grain rice will have a longer cylindrical shape, whereas short grain rice will be shorter and wider.
</p></li>

<li>
<p>
<strong>Long Grain Rice</strong> – This rice has milled grains that are at least three to four times as long as they are wide. Due to its starch composition, it is separate, light and fluffy when cooked.
</p>
<li>
<p>
<strong>Medium Grain Rice</strong> – When compared to long grain rice, medium grain rice has a shorter, wider kernel. Since the cooked grains are more moist and tender than long grain rice, the rice has a greater tendency to stick together.
</p></li>

<li>
<p>
<strong>Short Grain Rice</strong> – Featuring grains that are less than twice as long as they are wide, this rice is short and best for sushi. It has a sticky texture when cooked.
</p></li>


<h4 class="mb-4">Texture</h4>

<li>
<p>
When cooking rice dishes, you’ll want to think about the desired texture of the rice. The starch content varies from rice type to rice type. It will affect whether rice is sticky or light and fluffy.
</p></li>

<li>
<p>
<strong>Sticky Rice</strong> – Also known as sweet rice, sticky rice is grown mainly in Southeast and East Asia and is used in many traditional Asian dishes, desserts, and sweets. When cooked, sticky rice is especially sticky and is often ground into rice flour.
</p></li>

<li>
<p>
<strong>Parboiled Rice</strong> – This “rough” rice has gone through a steam-pressure process before milling that gelatinizes the starch in the grain. This process produces a more separate grain that is light and fluffy when cooked. Converted rice is a type of parboiled rice that has been further pre-cooked, which ultimately allows you to whip up dishes of rice even faster.
</p></li>


<h4 class="mb-4">Color</h4>

<li>
<p>
Rice is naturally brown after harvesting, but once the nutrient-rich outer layer of bran is removed, it is white in color. Red rice, black rice, and purple rice all feature unique pigmentation in the bran. For these colorful rice varieties, the bran layer usually remains for added visual appeal and added nutritional value.
</p></li>

<li>
<p>
<strong>Polished Rice</strong> – The term “polished” simply refers to white rice that has had its outer brown layer of bran and germ removed. Rice that has shed its bran layers can also be referred to as “milled rice.”
</p></li>

<li>
<p>
<strong>Brown Rice</strong> – This healthful rice sheds its outer husk and retains its bran and germ layers that give it a characteristic tan color. Though brown rice takes a little longer to cook than white rice, the nutrient-dense layers are rich in vitamins and minerals.
</p>
</li>

<li>
<p>
<strong>Forbidden Rice</strong> – High in nutritional value, this rice is also known as black rice and has a mild nutty flavor. Slightly sticky when cooked, it is used in a variety of Chinese or Thai dishes, including Chinese black rice cake and mango sticky rice. Mix it with white rice, and it also adds color to any rice pilaf or rice bowl.
</p>
</li>
<li>
<p>
<strong>Wild Rice</strong> – Wild rice grains are harvested from the genus Zizania of grasses. High in protein, wild rice adds a colorful, exotic flair to any rice dish. Serve it with stir frys, mushroom soups, or casseroles for something new.
</p>
</li>

<h4 class="mb-4">Aroma</h4>

<li>
<p>
Aroma is another factor to consider when cooking with rice. Certain rice varieties give off pleasing fragrances while being cooked. Add a sensory element to your guests’ dining experience with these rice types.
</p>
</li>

<li>
<p>
<strong>Basmati Rice</strong> – Basmati rice is a type of long-grain rice that is popular among Indian cuisine and other ethnic dishes. Cooked basmati rice imparts a subtle nutty or popcorn-like flavor and aroma.
</p>
</li>

<li>
<p>
<strong>Jasmine Rice</strong> – Jasmine rice, sometimes known as Thai fragrant rice, is a type of long grain rice with a long kernel and slightly sticky texture when cooked. Use it to infuse a subtle jasmine flavor and aroma into your dishes.
</p>
</li>
</ul>


<h3 class="mt-6 mb-4">Jasmine Rice</h3>

<h4 class="mb-4">White Rice</h4>
<img src="img/jasmine-whiterice.png" class="img-fluid float-right w50">
<p>
White rice is milled rice that has had its husk, bran, and germ removed. This alters the flavor, texture and appearance of the rice and helps prevent spoilage and extend its storage life. After milling, the rice is polished, resulting in a seed with a bright, white, shiny appearance.
</p>
<p>
The milling and polishing processes both remove nutrients. A diet based on unenriched white rice leaves many people vulnerable to the neurological disease beriberi, due to a deficiency of thiamine (vitamin B1). White rice is often enriched with some of the nutrients stripped from it during its processing. Enrichment of white rice with B1, B3, and iron is required by law in the United States. As with all natural foods, the precise nutritional composition of rice varies slightly depending on the variety, soil conditions, environmental conditions and types of fertilizers.
</p>
<p>
Adopted over brown rice in the second half of the 19th century because it was favored by traders, white rice has led to a beriberi epidemic in Asia.
</p>
<p>
At various times, starting in the 19th century, brown rice and wild rice have been advocated as more healthful alternatives. The bran in brown rice contains significant dietary fiber and the germ contains many vitamins and minerals.
</p>

<p>
Typically, 100 grams of uncooked rice produces around 240 to 260 grams of cooked grains, the difference in weight being due to absorbed cooking water.
</p>


          <h4 class="mt-6 mb-4">Basmathi Rice</h4>
          <p>
            Basmati is a variety of long, slender-grained aromatic rice which is traditionally from the Indian subcontinent.
          </p>
          <img src="./img/ricebg.png" class="img-fluid float-right w50">
          <p>
            According to the Indian Government agency APEDA, a rice variety is eligible to be notified as Basmati if it has a minimum average precooked milled rice length of 6.61 mm and average precooked milled rice breadth of up to 2 mm, among other parameters.</p>

          <h4 class="mb-4">Aroma and flavor</h4>


          <p>
            Basmati rice has a typical pandan-like (Pandanus amaryllifolius leaf) flavor caused by the aroma compound 2-acetyl-1-pyrroline. Basmati grains contain about 0.09 ppm of this aromatic chemical compound naturally, a level that is about 12 times more than non-basmati rice varieties, giving basmati its distinctive spicy fragrance and flavor. This natural aroma is also found in cheese, fruit and other cereals. It is a flavoring agent approved in the United States and Europe, and is used in bakery products for aroma.</p>


          <p>
            During cooking, the level of 2-acetyl-1-pyrroline decreases. Soaking the rice for 30 minutes before cooking permits 20% shorter cooking times and preserves more of the 2-acetyl-1-pyrroline.</p>

          <h4 class="mb-4">Glycemic index</h4>


          <p>
            According to the Canadian Diabetes Association, basmati, brown, wild, short and long grain rice has a “medium” glycemic index (between 56 and 69) opposed to jasmine and “instant” white rice with a glycemic index of 89, thus making it more suitable for diabetics as compared to certain other grains and products made from white flour.</p>




        <p> A mango is a juicy stone fruit (drupe) produced from numerous species of tropical trees belonging to the flowering plant genus Mangifera, cultivated mostly for their edible fruit.
        </p>
        <p>
          Most of these species are found in nature as wild mangoes. The genus belongs to the cashew family Anacardiaceae. Mangoes are native to South Asia, from where the “common mango” or “Indian mango”, Mangifera indica, has been distributed worldwide to become one of the most widely cultivated fruits in the tropics. Other Mangifera species (e.g. horse mango, Mangifera foetida) are grown on a more localized basis.
        </p>
        <p>
          Worldwide, there are several hundred cultivars of mango. Depending on the cultivar, mango fruit varies in size, shape, sweetness, skin color, and flesh color which may be pale yellow, gold, or orange. Mango is the national fruit of India and Pakistan, and the national tree of Bangladesh. It is the unofficial national fruit of the Philippines.
        </p>
        <h3 class=" mb-4">Cultivation </h3>
        <p>
          Mango trees grow to 35–40 m (115–131 ft) tall, with a crown radius of 10 m (33 ft). The trees are long-lived, as some specimens still fruit after 300 years. In deep soil, the taproot descends to a depth of 6 m (20 ft), with profuse, wide-spreading feeder roots and anchor roots penetrating deeply into the soil. The leaves are evergreen, alternate, simple, 15–35 cm (5.9–13.8 in) long, and 6–16 cm (2.4–6.3 in) broad; when the leaves are young they are orange-pink, rapidly changing to a dark, glossy red, then dark green as they mature. The flowers are produced in terminal panicles 10–40 cm (3.9–15.7 in) long; each flower is small and white with five petals 5–10 mm (0.20–0.39 in) long, with a mild, sweet fragrance. Over 500 varieties of mangoes are known, many of which ripen in summer, while some give a double crop. The fruit takes four to five months from flowering to ripen.
        </p>
        <p>
          The ripe fruit varies according to cultivar in size, shape, color, sweetness, and eating quality.Depending on cultivar, fruits are variously yellow, orange, red, or green. The fruit has a single flat, oblong pit that can be fibrous or hairy on the surface, and does not separate easily from the pulp. The fruits may be somewhat round, oval, or kidney-shaped, ranging from 5–25 centimetres (2–10 in) in length and from 140 grams (5 oz) to 2 kilograms (5 lb) in weight per individual fruit. The skin is leather-like, waxy, smooth, and fragrant, with color ranging from green to yellow, yellow-orange, yellow-red, or blushed with various shades of red, purple, pink or yellow when fully ripe.
        </p>
        <p>
          Ripe intact mangoes give off a distinctive resinous, sweet smell. Inside the pit 1–2 mm (0.039–0.079 in) thick is a thin lining covering a single seed, 4–7 cm (1.6–2.8 in) long. Mangoes have recalcitrant seeds which do not survive freezing and drying. Mango trees grow readily from seeds, with germination success highest when seeds are obtained from mature fruits.
        </p>
        <h3 class=" mb-4">Food constituents</h3>
        <ul>
          <h4 class=" mb-4">Nutrients</h4>
          <li>
            <p>
              The energy value per 100 g (3.5 oz) serving of the common mango is 250 kJ (60 kcal), and that of the apple mango is slightly higher (330 kJ (79 kcal) per 100 g). Fresh mango contains a variety of nutrients (right table), but only vitamin C and folate are in significant amounts of the Daily Value as 44% and 11%, respectively.
            </p>
          </li>
          <h4 class=" mb-4">Phytochemicals</h4>
          <li>
            <p>
              Numerous phytochemicals are present in mango peel and pulp, such as the triterpene, lupeol which is under basic research for its potential biological effects.
            </p>
          </li>
          <li>
            <p>
              Mango peel pigments under study include carotenoids, such as the provitamin A compound, beta-carotene, lutein and alpha-carotene, and polyphenols, such as quercetin, kaempferol, gallic acid, caffeic acid, catechins and tannins. Mango contains a unique xanthonoid called mangiferin.
            </p>
          </li>
          <li>
            <p>
              Phytochemical and nutrient content appears to vary across mango cultivars. Up to 25 different carotenoids have been isolated from mango pulp, the densest of which was beta-carotene, which accounts for the yellow-orange pigmentation of most mango cultivars. Mango leaves also have significant polyphenol content, including xanthonoids, mangiferin and gallic acid.
            </p>
          </li>
          <li>
            <p>
              The pigment euxanthin, known as Indian yellow, is often thought to be produced from the urine of cattle fed mango leaves; the practice is described as having been outlawed in 1908 because of malnutrition of the cattle and possible urushiol poisoning. This supposed origin of euxanthin appears to rely on a single, anecdotal source, and Indian legal records do not outlaw such a practice
            </p>
          </li>
        </ul>
